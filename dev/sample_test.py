def add2num(a,b):
    """
    >>> 2+5
    7
    >>> -4+3
    1
    """
    return a+b


def multiply(a,b):
    """
    >>> multiply(2, 3)
    6
    >>> multiply(1, -6)
    -6
    """
    return a*b


def power(a, b):
    """
    >>> power(2, 5)
    32
    >>> power(-1, 5)
    -1
    """
    return a**b